// Yield pauses the specified job and places it back in the job queue, behind other jobs of equal or higher importance,
// based on priority. (Only for use internally with Scope job type.) This method may poll for completion. Polling can
// be canceled by passing the cancel channel argument. The channel will be used to cancel polling and any outstanding
// HTTP requests.
//
// accountName is the Azure Data Lake Analytics account to execute job operations on. jobIdentity is job identifier.
// Uniquely identifies the job across all jobs submitted to the service.
func (client GroupClient) Yield(accountName string, jobIdentity uuid.UUID, cancel <-chan struct{}) (<-chan autorest.Response, <-chan error) {
	resultChan := make(chan autorest.Response, 1)
	errChan := make(chan error, 1)
	go func() {
		var err error
		var result autorest.Response
		defer func() {
			if err != nil {
				errChan <- err
			}
			resultChan <- result
			close(resultChan)
			close(errChan)
		}()
		req, err := client.YieldPreparer(accountName, jobIdentity, cancel)
		if err != nil {
			err = autorest.NewErrorWithError(err, "job.GroupClient", "Yield", nil, "Failure preparing request")
			return
		}

		resp, err := client.YieldSender(req)
		if err != nil {
			result.Response = resp
			err = autorest.NewErrorWithError(err, "job.GroupClient", "Yield", resp, "Failure sending request")
			return
		}

		result, err = client.YieldResponder(resp)
		if err != nil {
			err = autorest.NewErrorWithError(err, "job.GroupClient", "Yield", resp, "Failure responding to request")
		}
	}()
	return resultChan, errChan
}

